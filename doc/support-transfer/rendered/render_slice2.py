    "`CX_` records. Claude Code owns `CLAUDE.md`, `.claude/`, and `CC_` records. "
    "Coordination records without an agent prefix, schemas, templates, validators, "
    "claims, the generated board, support logs, and handoffs are shared-neutral by "
