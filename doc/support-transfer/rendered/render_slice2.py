allowed_guidance_placeholders = {
    "PROJECT_NAME", "CODEX_READ_ORDER", "CLAUDE_READ_ORDER",
    "CODEX_OWNERSHIP_TEXT", "CLAUDE_OWNERSHIP_TEXT",
    "CODEX_VALIDATION_TEXT", "CLAUDE_VALIDATION_TEXT",
    "CODEX_RECOVERY_TEXT", "CLAUDE_RECOVERY_TEXT",
    "CODEX_GATE_TEXT", "CLAUDE_GATE_TEXT",
    "READ_ORDER_DIFFERENCE", "OWNERSHIP_DIFFERENCE",
    "VALIDATION_DIFFERENCE", "RECOVERY_DIFFERENCE", "GATE_DIFFERENCE",
    "READ_ORDER_CHECK", "OWNERSHIP_CHECK", "VALIDATION_CHECK",
    "RECOVERY_CHECK", "GATE_CHECK",
}
placeholder_re = re.compile(r"\{\{([A-Z_]+)\}\}")
errors: list[str] = []
for rel, text in outputs.items():
    found = set(placeholder_re.findall(text))
    if rel == "coordination/templates/guidance-semantics.template.md":
        if found != allowed_guidance_placeholders:
            errors.append(f"{rel}: intentional placeholder set mismatch: {sorted(found)}")
    elif found:
        errors.append(f"{rel}: unresolved placeholders {sorted(found)}")
    scan_text = text
    if rel == "scripts/_support_shared.py":
        # The scanner necessarily contains its own credential/private-path regex
        # signatures. Exclude exactly that declaration, not the surrounding code.
        scan_text, removed = re.subn(
            r"SENSITIVE_PATTERNS:.*?\n\]\n",
            "",
            text,
            count=1,
            flags=re.DOTALL,
        )
        if removed != 1:
            errors.append(f"{rel}: sensitive-pattern declaration not found exactly once")
    hits = scan_sensitive(scan_text)
    if hits:
        errors.append(f"{rel}: sensitive patterns {hits}")
    for token in ("LLM_Wiki", "03_PKE", "Enconet", "xPY"):
        if token in text:
            errors.append(f"{rel}: forbidden workspace reference {token!r}")
if errors:
    raise AssertionError("\n".join(errors))

expected_paths = set(outputs) | {"coordination/BOARD.md"}
with tempfile.TemporaryDirectory(prefix="slice2-render-") as tmp:
    target = Path(tmp)
    for rel, text in outputs.items():
        path = target / rel
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(text, encoding="utf-8", newline="\n")

    command = [
        sys.executable,
        str(target / "scripts/agent_coord.py"),
        str(target),
        "--write-board",
        "--timestamp",
        NOW,
    ]
    completed = subprocess.run(command, text=True, capture_output=True, check=False)
    if completed.returncode != 0:
        raise AssertionError(
            "target validator failed in disposable root\n"
            + completed.stdout
            + completed.stderr
        )
    actual_paths = {
        path.relative_to(target).as_posix()
        for path in target.rglob("*")
        if path.is_file() and "__pycache__" not in path.parts
    }
    if actual_paths != expected_paths:
        raise AssertionError(
            f"inventory mismatch: expected {sorted(expected_paths)}, got {sorted(actual_paths)}"
        )
    board = (target / "coordination/BOARD.md").read_text(encoding="utf-8")
    required_board = (
        "- none",
        "- HANDOFF.md missing",
        "- Archive: 0 records in `coordination/archive/`",
        f"Generated: {NOW}",
    )
    for required in required_board:
        if required not in board:
            raise AssertionError(f"BOARD.md missing required initial state: {required}")

    link_re = re.compile(r"\]\(([^)#\s]+)")
    for path in target.rglob("*.md"):
        text = path.read_text(encoding="utf-8")
        for link in link_re.findall(text):
            if link.startswith(("http://", "https://")):
                continue
            resolved = (path.parent / link).resolve()
            try:
                resolved.relative_to(target)
            except ValueError:
                errors.append(f"{path.relative_to(target)}: escaping link {link!r}")
                continue
            if not resolved.exists() and not (FIN / resolved.relative_to(target)).exists():
                errors.append(f"{path.relative_to(target)}: dangling link {link!r}")
    if errors:
        raise AssertionError("\n".join(errors))

    if OUT.exists():
        shutil.rmtree(OUT)
    shutil.copytree(target, OUT, ignore=shutil.ignore_patterns("__pycache__"))

for rel in sorted(expected_paths):
    print("rendered", rel)
print("TARGET VALIDATION PASSED: 0 errors, 0 warnings")
print("INVENTORY PASSED: 14 exact files; HANDOFF.md missing is explicit; archive count is 0")
print(f"RENDER_TIMESTAMP={NOW}")
